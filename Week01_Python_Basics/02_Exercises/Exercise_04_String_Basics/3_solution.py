# Create first_name and last_name variables
first_name = "John"
last_name = "Doe"

# Create full_name by combining first_name and last_name
full_name = first_name + " " + last_name

# Calculate the length of full_name
name_length = len(full_name)

# Print the information as required
print(f"First name: {first_name}")
print(f"Last name: {last_name}")
print(f"Full name: {full_name}")
print(f"Name length: {name_length} characters")
